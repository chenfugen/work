			<h4>众安在线财产保险股份有限公司<br/>手术人身意外伤害保险条款<br/>（众安备-意外【2015】主77号）</h4>
			<p style="text-align: center;">总则</p>
			<p>第一条本保险合同由保险条款、投保单、保险单以及批单组成。凡涉及本保险合同的约定，均应采用书面形式。</p>
			<p>第二条除本保险合同另有约定外，在保险人认可的医院（见释义一）接受手术治疗的自然人可作为本保险合同的被保险人。无民事行为能力人和限制民事行为能力人不能作为本保险的被保险人，但其父母同意为未成年子女投保本保险合同的不受此限。对未成年人死亡给付的保险金总和不得超过国务院保险监督管理机构规定的限额。</p>
			<p>第三条本保险合同的投保人应为具有完全民事行为能力的被保险人本人、对被保险人有保险利益的其他人或对被保险人有保险利益的机关、企业、事业单位和社会团体。</p>
			<p>第四条订立本保险合同时，被保险人或投保人可指定一人或数人为身故保险金受益人。身故保险金受益人为数人时，应确定其受益顺序和受益份额；未确定受益份额的，各身故保险金受益人按照相等份额享有受益权。</p>
			<p>被保险人死亡后，有下列情形之一的，保险金作为被保险人的遗产，由保险人依照《中华人民共和国继承法》的规定履行给付保险金的义务：</p>
			<p>1、没有指定受益人，或者受益人指定不明无法确定的；</p>
			<p>2、受益人先于被保险人死亡，没有其他受益人的；</p>
			<p>3、受益人依法丧失受益权或者放弃受益权，没有其他受益人的。</p>
			<p>受益人与被保险人在同一事件中死亡，且不能确定死亡先后顺序的，推定受益人死亡在先。</p>
			<p>被保险人或投保人可以变更身故保险金受益人，但需书面通知保险人，由保险人在本保险合同上批注。对因身故保险金受益人变更发生的法律纠纷，保险人不承担任何责任。</p>
			<p>投保人指定或变更身故保险金受益人的，应经被保险人书面同意。被保险人为无民事行为能力人或限制民事行为能力人的，应由其监护人指定或变更身故保险金受益人。</p>
			<p>除另有约定外，本保险合同的伤残保险金的受益人为被保险人本人。</p>
			<p>第五条本保险合同的保险人为众安在线财产保险股份有限公司。</p>
			<p style="text-align: center;">保险责任</p>
			<p>第六条投保人可选择第七条所列一项或数项进行投保，保险人承保的项目以保险合同记载的为准，且保险人在约定的保险金额以内承担赔偿责任。</p>
			<p>第七条在保险期间内，被保险人在首次接受手术过程中，保险人对被保险人遭遇的以 下三类意外伤害事故承担保险责任：</p>
			<p>A类：医疗事故（见释义二）；</p>
			<p>B类：因施行医疗手术发生麻醉意外（见释义三）；</p>
			<p>C类：医疗意外（见释义四）。</p>
			<p>被保险人在本合同有效期内遭受上述意外伤害事故，保险人按下列规定给付保险金：</p>
			<p>（一）身故保险责任</p>
			<p>被保险人自A类或B类或C类意外伤害事故发生之日起90日内因该意外伤害事故身故的，保险人按本保险合同上所载的保险金额给付身故保险金，对该被保险人的保险责任终止，本合同终止。</p>
			<p>被保险人意外身故前保险人已给付本保险合同下述第（二）款约定的残疾保险金的，给付身故保险金时应扣除已给付的残疾保险金。</p>
			<p>（二）残疾保险责任</p>
			<p>被保险人自A类或B类或C类意外伤害事故发生之日起90日内因该意外伤害事故造成本保险合同所附《人身保险伤残评定标准》（见释义五）（简称《评定标准》）所列伤残程度之一的，保险人按《评定标准》所对应伤残等级的给付比例乘以本保险合同上所载的保险金额给 付残疾保险金。如第90日治疗仍未结束的，按当日的身体情况进行伤残评定，并据此给付残疾保险金。
			</p>
			<p>（1）被保险人因同一意外伤害事故造成两处或两处以上伤残时，保险人根据《评定标准》规定的多处伤残评定原则给付残疾保险金。</p>
			<p>（2）被保险人如在本次意外伤害之前已有残疾，保险人按合并后的残疾程度在《评定标准》中所对应伤残等级的给付比例扣除原有残疾程度在《评定标准》中所对应伤残等级的给付比例，给付残疾保险金。</p>
			<p style="text-align: center;">责任免除</p>
			<p>第八条本保险合同对下列任何情形所引起的损失、费用或责任不承担赔偿责任：</p>
			<p>（一）保险单或其他保险凭证中特别约定的保险人不承担保险责任的事项；</p>
			<p>（二）投保人或受益人对被保险人的故意杀害、故意伤害；</p>
			<p>（三）被保险人故意犯罪或者抗拒依法采取的刑事强制措施、自杀、故意自伤；</p>
			<p>（四）被保险人未遵医嘱服用、涂用、注射药物而导致的意外伤害；</p>
			<p>（五）输血感染；</p>
			<p>（六）正常治疗发生的副作用及难以避免的并发症；</p>
			<p>（七）非本次手术治疗范围内的投保前已患病症；</p>
			<p>（八）被保险人拒绝或者未按要求配合检查、治疗；</p>
			<p>（九）被保险人在非手术医院另行诊断或治疗；</p>
			<p>（十）被保险人患有艾滋病（AIDS）或感染艾滋病病毒（HIV）（见释义六）；</p>
			<p>（十一）被保险人吸毒、酗酒或受酒精、毒品、管制药物的影响；</p>
			<p>（十二）战争（见释义七）、军事冲突（见释义八）、暴乱（见释义九）或武装叛乱；</p>
			<p>（十三）核爆炸、核辐射或核污染。</p>
			<p>第九条其他不属于本保险责任范围内的损失、费用和责任，保险人不负责赔偿。</p>
			<p style="text-align: center;">保险金额</p>
			<p>第十条保险金额是保险人承担给付保险金责任的最高限额。保险金额由投保人、保险人双方约定，并在保险单中载明。</p>
			<p style="text-align: center;">保险期间</p>
			<p>第十一条除另有约定外，保险责任起讫期自被保险人进入手术室开始，至手术结束后24小时止。但保险期间最长不超过30天。</p>
			<p style="text-align: center;">保险人义务</p>
			<p>第十二条本保险合同成立后，保险人应当及时向投保人签发保险单或其他保险凭证。</p>
			<p>第十三条保险人在保险合同订立时已经知道投保人未如实告知的情况的，保险人不得解除合同；发生保险事故的，保险人应当承担赔偿责任。</p>
			<p>第十四条保险事故发生后，投保人、被保险人提供的有关索赔的证明和资料不完整的，保险人应当及时一次性通知投保人、被保险人补充提供。</p>
			<p>第十五条保险人收到被保险人或者受益人的赔偿或者给付保险金的请求后，应当及时 作出核定；情形复杂的，应当在三十日内作出核定，但合同另有约定的除外。保险人应当将 核定结果通知被保险人或者受益人；对属于保险责任的，在与被保险人或者受益人达成赔偿 或者给付保险金的协议后十日内，履行赔偿或者给付保险金义务。保险合同对赔偿或者给付 保险金的期限有约定的，保险人应当按照约定履行赔偿或者给付保险金义务。保险人依照前 款的规定作出核定后，对不属于保险责任的，应当自作出核定之日起三日内向被保险人发出 拒绝赔偿保险金通知书，并说明理由。</p>
			<p>第十六条保险人自收到赔偿保险金的请求和有关证明、资料之日起六十日内，对其赔 偿保险金的数额不能确定的，应当根据已有证明和资料可以确定的数额先予支付；保险人最 终确定赔偿的数额后，应当支付相应的差额。</p>
			<p style="text-align: center;">投保人、被保险人义务</p>
			<p>第十七条投保人应履行如实告知义务，如实回答保险人就保险标的或被保险人的有关 情况提出的询问，并如实填写投保单。</p>
			<p>投保人故意或者因重大过失未履行前款规定的如实告知义务，足以影响保险人决定是否 同意承保或者提高保险费率的，保险人有权解除合同。</p>
			<p>保险人依本保险条款取得的合同解除权，自保险人知道有解除事由之日起，超过三十日不行使而消灭。</p>
			<p>投保人故意不履行如实告知义务的，保险人对于合同解除前发生的保险事故，不承担赔偿或者给付保险金的责任，并不退还保险费。</p>
			<p>投保人因重大过失未履行如实告知义务，对保险事故的发生有严重影响的，保险人对于合同解除前发生的保险事故，不承担赔偿或者给付保险金的责任，但应当退还保险费。</p>
			<p>保险人在本保险合同订立时已经知道投保人未如实告知的情况的，保险人不得解除合同；发生保险事故的，保险人应当承担给付保险金责任。</p>
			<p>第十八条投保人、被保险人或者保险金受益人知道保险事故发生后，应当及时通知保险人，并书面说明事故发生的原因、经过和损失情况。故意或者因重大过失未及时通知，致使保险事故的性质、原因、损失程度等难以确定的，保险人对无法确定的部分，不承担给付保险金责任，但保险人通过其他途径已经及时知道或者应当及时知道保险事故发生的除外。</p>
			<p>上述约定，不包括因不可抗力而导致的迟延。</p>
			<p>第十九条发生保险事故，被保险人或索赔人向保险人申请赔偿时，应提供下列文件或资料。保险申请人（见释义十）因特殊原因不能提供以下材料的，应提供其他合法有效的材料。保险金申请人未能提供有关材料，导致保险人无法核实该申请的真实性的，保险人对无法核实部分不承担给付保险金的责任。</p>
			<p>（1）保险金给付申请书；</p>
			<p>（2）保险合同或其他保险凭证；</p>
			<p>（3）申请人的有效身份证件；</p>
			<p>（4）被保险人病历中的手术记录；</p>
			<p>（5）公安部门或司法部门、二级及二级以上医院或保险人认可的医疗机构出具的被保 险人死亡证明或验尸报告或保险人认可的医疗机构或其他鉴定机构出具的残疾鉴定诊断书；
			</p>
			<p>（6）若被保险人身故,被保险人的户籍注销证明；</p>
			<p>（7）保险金申请人所能提供的与确认保险事故的性质、原因、损失程度等有关的其他 证明和资料；</p>
			<p>（8）若保险金申请人委托他人申请的，还应提供授权委托书原件、委托人和受托人的 身份证明等相关证明文件。受益人为无民事行为能力人或者限制民事行为能力人的，由其监 护人代为申领保险金，并需要提供监护人的身份证明等资料；</p>
			<p>（9）因医疗意外或麻醉意外导致保险事故发生的，申请人还需提供手术医院出具的意 外事故证明材料；</p>
			<p>（10）因医疗事故导致保险事故发生的，申请人还需提供国家认可的医疗事故鉴定机 构出具的医疗事故技术鉴定书或人民法院出具的医疗事故认定的判决书。 第二十条在本保险合同成立后，除医院通知撤销手术外，投保人不得解除合同。 如医院通知撤销手术，投保人办理解除合同手续时，应提供下列证明和材料：</p>
			<p>第二十条在本保险合同成立后，除医院通知撤销手术外，投保人不得解除合同。如医院通知撤销手术，投保人办理解除合同手续时，应提供下列证明和材料：</p>
			<p>（1）保险合同解除申请书；</p>
			<p>（2）保险合同或其他保险凭证；</p>
			<p>（3）保险费交付凭证；</p>
			<p>（4）解除合同通知书；</p>
			<p>（5）医院通知撤销手术的书面证明；</p>
			<p>（6）投保人身份证明或投保单位证明。</p>
			<p> 自保险人接到解除合同通知书之时起，本保险合同的效力终止。保险人收到上述证明和 材料之日起30日内，退还保险费。</p>
			<p>第二十一条在保险期间内，经投保人与保险人双方约定，可以采用附加批注或批单的 方式变更本保险合同的有关内容。附加批注或批单是本保险合同的有效组成部分，本保险合 同条款与附加批注或批单不一致之处，以附加批注或批单为准，附加批注或批单未尽之处， 以本保险合同条款为准。</p>
			<p style="text-align: center;">诉讼时效和争议处理</p>
			<p>第二十二条被保险人向保险人请求赔偿的诉讼时效期间为二年，自其知道或者应当知道保险事故发生之日起计算。</p>
			<p>第二十三条因履行本保险合同发生争议，由当事人协商解决。协商不成的，提交保险单载明的仲裁机构仲裁；保险单未载明仲裁机构且争议发生后未达成仲裁协议的，依法向人民法院起诉。</p>
			<p>第二十四条本保险合同的争议处理适用中华人民共和国法律（不包括香港、澳门及台湾地区法律）。</p>
			<p style="text-align: center;">释义</p>
			<p>一、医院:指保险人与被保险人约定的定点医院，未约定定点医院的，则指经中华人民共和国卫生部门评审确定的二级或二级以上的公立医院。不包括康复医院或康复病房、精神病院、疗养院、护理院、戒酒或戒毒中心、精神心理治疗中心、急诊或门诊观察室、无相应医护人员或设备的二级或三级医院的联合医院或联合病房。</p>
			<p>二、医疗事故：指医疗机构及其医务人员在医疗活动中，违反医疗卫生管理法律、行政法规、部门规章和诊疗护理规范、常规，过失造成患者人身损害的事故。</p>
			<p>三、麻醉意外：指正常施行医疗手术麻醉过程中，医务人员按规章制度和有关技术操作 规程实施麻醉，但由于患者病情异常或体质特殊而发生难以预料和防范的意外，或在现有医 学科学技术条件下，发生无法预料或不能防范的不良后果。</p>
			<p>四、医疗意外：指医务人员在从事诊疗或护理工作过程中，由于患者的病情或患者体质 的特殊性而发生难以预料和防范的患者死亡、残疾或者功能障碍等不良后果的行为。</p>
			<p>五、《人身保险伤残评定标准》：指中国保险行业协会发布的人身保险伤残程度评定 与保险金给付比例标准，详见《关于印发
				<人身保险伤残评定标准>的通知》（中保协发【2013】 88号）。</p>
			<p>六、艾滋病（AIDS）或艾滋病病毒（HIV）：艾滋病病毒指人类免疫缺陷病毒，英文缩 写为HIV。艾滋病指人类免疫缺陷病毒引起的获得性免疫缺陷综合征，英文缩写为AIDS。 在人体血液或其它样本中检测到艾滋病病毒或其抗体呈阳性，没有出现临床症状或体征 的，为感染艾滋病病毒；如果同时出现了明显临床症状或体征的，为患艾滋病。</p>
			<p>七、战争：指国家与国家、民族与民族、政治集团与政治集团之间为了一定的政治、经 济目的而进行的武装斗争，以政府宣布为准。</p>
			<p>八、军事冲突：指国家或民族之间在一定范围内的武装对抗，以政府宣布为准。</p>
			<p>九、暴乱：指破坏社会秩序的武装骚乱，以政府宣布为准。</p>
			<p>十、保险金申请人：身故保险金申请人是指受益人或被保险人的继承人或依法享有保险 金请求权的其他自然人；残疾保险金申请人是指被保险人本人。</p>
